export const getProductById = async (id) => {
  const products = await getProducts();
  return products.find(product => product.id === parseInt(id));
};


export const getTopSellingProducts = async () => {
  // Récupérez les produits en appelant la fonction getProducts de manière asynchrone
  const products = await getProducts();

  // Vérifiez si les produits sont bien un tableau
  if (!Array.isArray(products)) {
    console.error('Les produits ne sont pas un tableau');
    return []; // Retournez un tableau vide en cas d'erreur
  }

  // Si les produits sont un tableau, retournez les 5 premiers éléments comme produits les plus vendus
  const topSellingProducts = products.slice(0, 5);

  return topSellingProducts;
};